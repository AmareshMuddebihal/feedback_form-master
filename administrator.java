    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        boolean boValid = true;
        jLabel8.setVisible(false);
        jLabel6.setVisible(true);
        jLabel6.setText("");
        jLabel6.setForeground(Color.green);
        if (jTextField1.getText().isEmpty())
        {
            jLabel8.setText("Faculty Name cannot be empty");
            jLabel8.setVisible(true);
            boValid = false;
        }
        if(jTextField2.getText().isEmpty())
        {
            jLabel9.setText("Subject Name cannot be empty");
            jLabel9.setVisible(true);
            boValid = false;
        }
        
        if (true == boValid)
        {
            //insert into database
            //use jlabel6 to show result (faculty details inserted successfully or failure error)
            try
            {
               Statement stmt;
               Connection con;
               Class.forName("com.mysql.jdbc.Driver");  
               con=DriverManager.getConnection("jdbc:mysql://localhost:3306/feedback","root","shivani");  
               if (null != con)
               {
                   stmt=con.createStatement();  
                   if (null != stmt)
                   {
                        String faculty = "insert into faculty_details(name, course, department, subject, year, division) values("+
                                "'"+jTextField1.getText()+
                                "','"+jComboBox1.getSelectedItem()+
                                "','"+jComboBox2.getSelectedItem()+
                                "','"+jTextField2.getText()+
                                "','"+jComboBox3.getSelectedItem()+
                                "','"+jComboBox4.getSelectedItem()+
                                "')";
                        stmt.executeUpdate(faculty);
                        stmt.close();
                        jLabel6.setText("Faculty detail added successfully");
                   }
                   else
                   {
                        jLabel6.setForeground(Color.red);
                        jLabel6.setText("Operation not successful!! Record could not be inserted.");
                   }
               }

               if (null != con)
                   con.close();
           }
           catch(Exception sql)
           {
               jLabel6.setText("Exception: "+ sql.getMessage());
           }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

