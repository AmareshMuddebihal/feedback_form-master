    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         
        try
        {
           Statement stmt;
           Connection con;
           jLabel10.setVisible(true);
           jLabel10.setText("");
           String question = jTextArea2.getText();
           if (question.isEmpty())
           {
               jLabel10.setText("Operation not successful!!");
               return;
           }
           Class.forName("com.mysql.jdbc.Driver");  
           con=DriverManager.getConnection("jdbc:mysql://localhost:3306/feedback","root","123456");  
           if (null != con)
           {
               stmt=con.createStatement();  
               if (null != stmt)
               {
                    String ques = "insert into feedback_ques(question) values('"+question+"')";
                    stmt.executeUpdate(ques);
                    stmt.close();
                    jLabel10.setText("Question added successfully");
               }
               else
               {
                   jLabel10.setText("Operation not successful!! Database cannot be connected.");
               }
           }

           if (null != con)
               con.close();
       }
       catch(Exception sql)
       {
           jLabel10.setText("Exception: "+ sql.getMessage());
       }
    }//GEN-LAST:event_jButton4ActionPerformed

